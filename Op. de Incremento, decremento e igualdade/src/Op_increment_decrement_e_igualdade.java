
public class Op_increment_decrement_e_igualdade {

	public static void main(String[] args) {
		int numero = 0;
		System.out.println(numero);
	
		   // numero = numero +1
		  // ++ = incremento soma 1
		 // -- = decremento subtrai 1
	    // == = utilizado para verificar igualdade
	   // != = utilizado para verificar diferen√ßa
		System.out.println(++numero);
		System.out.println(++numero);
		System.out.println(--numero);
	}

}
