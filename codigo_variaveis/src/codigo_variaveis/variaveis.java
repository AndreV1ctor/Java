package codigo_variaveis;

public class variaveis {

	public static void main(String[] args) {
		
		int numero = 10;
		System.out.println(numero);
		char letra = '1';
		System.out.println(letra);
		short numero2 = 11;
		System.out.println(numero2);
		boolean verdadeiro_falso = true;
		System.out.println(verdadeiro_falso);
		String frase;
		frase = new String ("Olá estou aprendendo Java");
		System.out.println(frase);
		byte numero3 = 5; //Os bytes só recebem os números de -128 até 127
		System.out.println(numero3);

	}

}
